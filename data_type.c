
// This program is used for understand Datatype how to accept the data's.

#include <stdio.h>

int main()
{
	int a = 10;

	printf("[INT] value of : %i\n",a);
	printf("[NUM] value of : %d\n",a);
	printf("[OCTAL] value of : %o\n",a);
	printf("[HEX] value of : %X\n",a);
	printf("[CHAR] value of : %c\n",a);
	printf("[ADDR] value of : %p\n",a);
	printf("[CAPITAL] value of : %E\n",a);

}
