// This program is used to How to Add values 


#include <stdio.h>

int main()
{
	int a,b;

	printf("Enter A value : "); 
	scanf("%d", &a);		// Get A value
	printf("Enter B value : "); 
	scanf("%d", &b);		// Get B value
	printf("A = %d \n",a);
	printf("B = %d \n",b);
	printf("Total = %d",a+b);	// Adding A and b value
}
